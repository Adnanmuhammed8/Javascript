// filter()

a = [1,2,3,4,5,6]
///w.a.p to find even numbers from given array
even = a.filter((num)=>num%2==0)
console.log(even);

// w.a.p to print numbers >= 4
numbers = a.filter((n1)=>n1>=4)
console.log(numbers);
console.log(`-------------------`);


// find()
//even
even = a.find((num)=>num%2==0)
console.log(even);
//numbers>=4
numbers = a.find((n1)=>n1>=4)
console.log(numbers);


